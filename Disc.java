package com.model;

public class Disc {
	
	String discname;
	String availbytes;
	/**
	 * @return the discname
	 */
	public String getDiscname() {
		return discname;
	}
	/**
	 * @param discname the discname to set
	 */
	public void setDiscname(String discname) {
		this.discname = discname;
	}
	/**
	 * @return the availbytes
	 */
	public String getAvailbytes() {
		return availbytes;
	}
	/**
	 * @param availbytes the availbytes to set
	 */
	public void setAvailbytes(String availbytes) {
		this.availbytes = availbytes;
	}
	
	

}
